    hec.set_defaults(fn=cmd_hub_export_config)
    hpc = hb_sp.add_parser("push-config")
    hpc.add_argument("--host", required=True)
    hpc.add_argument("--user", required=True)
    hpc.add_argument("--ssh-key", default="")
