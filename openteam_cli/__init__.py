    tc.add_argument("--dry-run", action="store_true")
    tc.set_defaults(fn=cmd_task_close)
    tsh = ts_sp.add_parser("ship")
    tsh.add_argument("task_id")
    tsh.add_argument("--scope")
    tsh.add_argument("--summary")
